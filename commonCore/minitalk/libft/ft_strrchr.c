#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	if (!s)
		return (NULL);
	i = ft_strlen(s);
	while (s[--i])
	{
		if (s[i] == (unsigned char) c)
			return ((char *)&s[i]);
	}
	return (NULL);
}
